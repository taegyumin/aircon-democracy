# ─────────────────────────────────────────────────────────────────────
# Build subway adjacency
# ─────────────────────────────────────────────────────────────────────

# 1. Load existing 수도권 adjacency and add city="서울"
existing_path = DATA / "subway-adjacency.json"
existing = json.loads(existing_path.read_text(encoding="utf-8"))

# Existing line name normalization: 의정부경전철 is the canonical name
# (stations.json uses "의정부선" - that side will be fixed separately)
# Existing data already uses "의정부경전철" - leave as is.

new_edges = []
seen = set()  # (from, to, line, city) - canonical with sorted pair

def add_edge(frm, to, line, city):
    key = tuple(sorted([frm, to])) + (line, city)
    if key in seen:
        return False
    seen.add(key)
    new_edges.append({"from": frm, "to": to, "line": line, "city": city})
    return True

for e in existing:
    add_edge(e["from"], e["to"], e["line"], "서울")

print(f"After porting existing 수도권: {len(new_edges)} edges")

# 2. Add edges from CHAINS (new regions + new lines)
for (city, line), branches in CHAINS.items():
    added = 0
    for chain in branches:
        for a, b in pairs(chain):
            if add_edge(a, b, line, city):
                added += 1
    print(f"  + {city} {line}: {added} edges")

# Sort for deterministic output (by city, line, from)
new_edges.sort(key=lambda e: (e["city"], e["line"], e["from"], e["to"]))

out_path = DATA / "subway-adjacency.json"
out_path.write_text(
    json.dumps(new_edges, ensure_ascii=False, separators=(", ", ": ")),
    encoding="utf-8",
)
print(f"\nWrote {out_path} with {len(new_edges)} edges")

