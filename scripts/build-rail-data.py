train_edges = []
seen_train = set()

def add_train(frm, to, line, operator):
    key = tuple(sorted([frm, to])) + (line, operator)
    if key in seen_train:
        return
    seen_train.add(key)
    train_edges.append({"from": frm, "to": to, "line": line, "operator": operator})

for (operator, line), chain in TRAIN_CHAINS.items():
    for a, b in pairs(chain):
        add_train(a, b, line, operator)

train_edges.sort(key=lambda e: (e["operator"], e["line"], e["from"], e["to"]))

train_path = DATA / "train-adjacency.json"
train_path.write_text(
    json.dumps(train_edges, ensure_ascii=False, separators=(", ", ": ")),
    encoding="utf-8",
)
print(f"Wrote {train_path} with {len(train_edges)} edges")
